        private static System.Net.Sockets.TcpClient socket;
        private static System.Net.Sockets.NetworkStream stream;
        private static object lockObject = new object();
        
        public static bool SendCommand(Byte[] cmd, out Byte[] recievedBuffer, int waitTime, string ip)
        {
            lock (lockObject)
            {
                recievedBuffer = new byte[0];

                try
                {
                    socket = new System.Net.Sockets.TcpClient();
                    socket.Connect(ip, 53484);
                    stream = socket.GetStream();

                    stream.Write(cmd, 0, cmd.GetLength(0));

                    // Waitが0以上の場合は戻りがあるはず
                    if (waitTime > 0)
                    {
                        System.Threading.Thread.Sleep(waitTime);

                        // 最大10秒追加で待つ
                        for (int count = 0; count < 100; count++)
                        {
                            if (socket.Available > 0)
                            {
                                Byte[] dat = new Byte[socket.Available];
                                stream.Read(dat, 0, dat.GetLength(0));

                                recievedBuffer = new byte[dat.Length];
                                Array.Copy(dat, recievedBuffer, dat.Length);

                                break;
                            }

                            // 受信が確認できない場合は100msec待機
                            //if (Settings.Ins.ExecLog == true)
                            //{ MainWindow.SaveExecLog("*** SDCP Wait 100[msec] (IP : " + ip + ", Count = " + count + ") ***"); }

                            System.Threading.Thread.Sleep(100);
                        }
                    }

                    socket.Close();
                    stream.Close();

                    return true;
                }
                catch (Exception ex)
                {
                    MainWindow.ShowMessageWindow(ex.Message, "Exception!", System.Drawing.SystemIcons.Error, 500, 210);
                    return false;
                }
            }
        }
    }
